package com.example.jeff.mtbtrailapp.Model;

public class Drink {
    private String drinkId;
    private String drinkName;
    private String drinkDirect;
    private String drinkIv;
    private String drinkIng;


    public Drink() {
    }

    public String getDrinkId() {
        return drinkId;
    }

    public void setDrinkId(String drinkId) {
        this.drinkId = drinkId;
    }

    public String getDrinkName() {
        return drinkName;
    }

    public void setDrinkName(String drinkName) {
        this.drinkName = drinkName;
    }

    public String getDrinkDirect() {
        return drinkDirect;
    }

    public void setDrinkDirect(String drinkDirect) {
        this.drinkDirect = drinkDirect;
    }

    public String getDrinkIv() {
        return drinkIv;
    }

    public void setDrinkIv(String drinkIv) {
        this.drinkIv = drinkIv;
    }

    public String getDrinkIng() {
        return drinkIng;
    }

    public void setDrinkIng(String drinkIng) {
        this.drinkIng = drinkIng;
    }
}
